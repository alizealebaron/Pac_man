from pydantic import BaseModel, Field


# +-------------------------------------------------------------------------+
# |                                 Classe                                  |
# +-------------------------------------------------------------------------+

class PokemonModel(BaseModel):

    name: str = Field(min_length=1)
    width: int = Field(ge=1)
    height: int = Field(ge=1)
    nb_anim: int = Field(ge=1)
    scale: float = Field(ge=1)
    comportement: str
