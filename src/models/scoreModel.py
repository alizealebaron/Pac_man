from pydantic import BaseModel, Field


# +-------------------------------------------------------------------------+
# |                                 Classe                                  |
# +-------------------------------------------------------------------------+

class Score(BaseModel):

    name: str = Field()
    width: int = Field(ge=1, le=500, default=10)
    height: int = Field(ge=1, le=500, default=10)
