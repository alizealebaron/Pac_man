from pydantic import BaseModel, Field


# +-------------------------------------------------------------------------+
# |                                 Classe                                  |
# +-------------------------------------------------------------------------+

class Score(BaseModel):

    # +---------------------------------------------------------------------+
    # |                              Attributs                              |
    # +---------------------------------------------------------------------+

    name: str = Field(min_length=1, max_length=20)
    score: int = Field(ge=0)
    pokemon: str = Field(min_length=1)
